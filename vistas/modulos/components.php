            <h2 class="text-center">¿Qué es el Hosting de una página web?</h2>
            <p>Si estás pensando en poner tu marca en línea a través de un sitio web, es importante tener algunos conocimientos generales en relación a información más técnica de una página web, como el caso del Hosting. <br> <br>

              Para que un sitio web funcione de forma correcta, es imprescindible tener un servidor web, una máquina que está conectada a internet, con un ancho de banda más amplio que las conexiones normales. <br> <br>

              Es importante además, que este aparato esté conectado las 24 horas del día y todos los días de la semana. <br> <br>

              En este caso, estos servidores proporcionan un servicio de almacenamiento, que aloja la página web y la hace funcionar en cualquier parte del mundo, a este servicio de almacenamiento le llamamos Hosting. <br> <br>

              Existen diferentes tipos de Hosting, los más conocidos son el Hosting Compartido y el Hosting Dedicado.
            </p>

            <ol class="features">
              <li>
                <small class="signature">Hosting Compartido </small>
                <p>
                  Este es el hosting más común que existe, suele ser más económico, la idea es que varios hosting comparten el mismo servidor, es decir que utilizan la misma memoria, disco duro y el mismo procesador. <br> <br>
                  La principal ventaja es que los gastos se comparten entre todos los que comparten el mismo hosting y no hacen falta tener conocimientos técnicos avanzados para gestionarlo.
                </p>
              </li>

              <li>
                <small class="signature">Hosting Dedicado</small>
                <p>
                  Se refiere a un tipo de hosting en el que todos los recursos de un servidor, se le asigna únicamente a un hosting, está destinado a sitios con muchas visitas, puesto que es bastante potente. <br> <br>
                  La principal desventaja es que suele ser un poco más caro que el Hosting Compartido, pero vale la pena por su nivel de potencia para correr algunos sitios web o aplicaciones empresariales que necesitan velocidad en los procesos y bastante espacio en la memoria para almacenar la información. <br> <br>
                </p>
              </li>
            </ol>

